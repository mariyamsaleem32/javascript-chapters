// Rounding numbers

console.log(Math.round(4.5));   // Math.round(4.5) will give you 5 because 4.5 is closer to 5 than 4.
console.log(Math.round(4.2));   // Math.round(4.2) will give you 4 because 4.2 is closer to 4 than 5.
console.log(Math.round(-4.5));  // Math.round(-4.5) will give you -4 because -4.5 is closer to -4 than -5. and it's negative number 

console.log(Math.ceil(3.5));    // Math.ceil(3.5) will give you 4 because it rounds 3.5 up to the nearest integer greater than or equal to 3.5.
console.log(Math.ceil(4.2));    // Math.ceil(4.2) will give you 5 because it rounds 4.2 up to the nearest integer greater than or equal to 4.2.
console.log(Math.ceil(-4.5));   // Math.ceil(-4.5) will give you -4 because it rounds -4.5 up to the nearest integer greater than or equal to -4.5.

console.log(Math.floor(8.5));   // Math.floor(8.5) will give you 8 because it rounds 8.5 down to the nearest integer less than or equal to 4.5
console.log(Math.floor(6.1));   // Math.floor(6.1) will give you 6 because it rounds 6.1 down to the nearest integer less than or equal to 6.1.
console.log(Math.floor(-4.5));  // Math.floor(-4.5) will give you -5 because it rounds -4.5 down to the nearest integer less than or equal to -4.5.



