// Converting strings to integers and decimals

var currentAge = prompt("Enter your age."); //enter 18
var qualifyingAge = currentAge + 1;
console.log(qualifyingAge);      // return 181

var currentAge = prompt("Enter your age.");  // enter 17
var qualifyingAge = parseInt(currentAge) + 1;
console.log(qualifyingAge);      // 17 + 1 = 18

